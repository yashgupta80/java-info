package info;

public class Operator {
    public static void main(String[] args){
        // learning operator....|
        // operands and expression...
        int a = 2;
        int result =  a+3;//adding two number
        // subs
        result = result-5;
        System.out.println("result is = " + result);

        // divide
        int b = 5;
        int ans = b/2;
        // multiple..
        ans = ans*5;
        System.out.println("answer is = " + ans);// ans is = 10;

        ++result;// result increment one time;
        --ans; // ans is decrement one time;

        System.out.println("result after increment = " + result); // result = 1;
        System.out.println("ans after decrement = " +ans); // ans = 9;






    }
}
