package info;

public class Dimension {
    private int height;
    private int wedth;
    private int depth;

    public Dimension(int height, int wedth, int depth) {
        this.height = height;
        this.wedth = wedth;
        this.depth = depth;
    }

    public int getHeight() {
        return height;
    }

    public int getWedth() {
        return wedth;
    }

    public int getDepth() {
        return depth;
    }



}
